package com.vishnu.sadgurunagarsevakendra.YuvaSenaSadgurunagar;

public class YuvaSenaUserClassHelper {
    String name,mobile,age,date,jangnan;

    public YuvaSenaUserClassHelper(String name, String mobile, String age, String date, String jangnan) {
        this.name = name;
        this.mobile = mobile;
        this.age = age;
        this.date = date;
        this.jangnan = jangnan;
    }

    public YuvaSenaUserClassHelper() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getJangnan() {
        return jangnan;
    }

    public void setJangnan(String jangnan) {
        this.jangnan = jangnan;
    }
}
