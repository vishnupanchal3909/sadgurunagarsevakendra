package com.vishnu.sadgurunagarsevakendra.BhudanSanklpa;

public class BhudanUserClassHelper {
    String name,mobile,jan;

    public BhudanUserClassHelper() {
    }

    public BhudanUserClassHelper(String name, String mobile, String jan) {
        this.name = name;
        this.mobile = mobile;
        this.jan = jan;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getJan() {
        return jan;
    }

    public void setJan(String jan) {
        this.jan = jan;
    }
}
